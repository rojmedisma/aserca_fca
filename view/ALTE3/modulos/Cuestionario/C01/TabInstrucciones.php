	<h4>Instrucciones para llenar el instrumento de autoevaluación.</h4>
	<h5>Fortalecimiento a los centros de acopio</h5>
	<h5 class="text-primary">¿Qué es este instrumento?</h5>
	<p>El presente instrumento es un cuestionario de autoevaluación, el cual permitirá a los operadores de los centros de acopio, a partir de la información que disponga; conocer cuáles son las fortalezas y áreas de oportunidad para mejorar la operación de sus centros de acopio.</p>
	<h5 class="text-primary">¿Qué mide?</h5>
	<p>Componentes claves para fortalecer la operación, seguridad y financiamiento de los centros de acopio, en 7 aspectos claves:</p>
	<ol>
		<li>Planificación estratégica</li>
		<li>Operación e infraestructura</li>
		<li>Administración y organización empresarial</li>
		<li>Gobierno corporativo</li>
		<li>Modelo de negocio</li>
		<li>Evaluación financiera</li>
		<li>Sistemas de control y riesgos</li>
	</ol>
	<p>Cada uno de estos componentes contribuyen, en mayor o menor medida, a que el centro de acopio opere con mejores resultados.</p>
	<h5 class="text-primary">¿Cómo llenar el instrumento de autoevaluación?</h5>
	<p>El cuestionario consta de 7 componentes, los cuales deben ser alimentados con la información que disponga el operador o administrador del centro de acopio. El cuestionario le irá conduciendo automáticamente a la siguiente sección que deberá responder.</p>
	<p>Al final del cuestionario se mostrarán los resultados en tres secciones:</p>
	<ol>
		<li><strong>Resultado general.</strong> Le compartirá a través de una gráfica radial - donde se integran todos los resultados de la evaluación de los componentes-; el nivel de cumplimiento de la operación del centro de acopio.</li>
		<li><strong>Resultados por componentes.</strong> Cada uno de los componentes, será evaluado y graficado de manera individual mostrando a mayor detalle, cuáles son los elementos que deben observarse y ser fortalecidos de manera particular.</li>
		<li><strong>Semáforo.</strong> Este instrumento le mostrará el nivel de operación que tiene su centro de acopio, en relación con el grado de cumplimiento que se requiere a partir de la guía de buenas prácticas, para el proceso operativo de los centros de acopio.</li>
	</ol>
	<p>La información generada, le permitirá tomar decisiones sobre los aspectos a fortalecer en su empresa.</p>
