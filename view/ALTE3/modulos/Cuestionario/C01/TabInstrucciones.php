	<h4>Instrucciones para llenar el cuestionario de autoevaluación.</h4>
	<h5>Fortalecimiento a los centros de acopio</h5>
	<h5 class="text-primary">¿Qué es este instrumento?</h5>
	<p>El presente instrumento es un cuestionario de autoevaluación, el cual permitirá a los operadores de los centros de acopio, a partir de la información que disponga; conocer cuáles son las fortalezas y áreas de oportunidad para mejorar la operación de sus centros de acopio.</p>
	<h5 class="text-primary">¿Qué mide?</h5>
	<p>Componentes claves para fortalecer la operación, seguridad y financiamiento de los centros de acopio, en 7 aspectos claves:</p>
	<ol>
		<li>Planeación estratégica</li>
		<li>Infraestructura y operación</li>
		<li>Administración y operación</li>
		<li>Gobierno corporativo</li>
		<li>Modelo de negocio</li>
		<li>Evaluación financiera</li>
		<li>Sistemas de control</li>
	</ol>
	<p>Cada uno de estos componentes contribuyen, en mayor o menor medida, a que el centro de acopio opere con mejores resultados.</p>
	<h5 class="text-primary">¿Cómo llenar el instrumento de diagnóstico?</h5>
	<p>El cuestionario consta de 7 componentes, las cuáles deben ser alimentadas obligatoriamente con la información que disponga el operador, administrador del centro de acopio. El cuestionario le irá conduciendo automáticamente a la siguiente hoja a registrar.</p>
	<p>Al final del cuestionario se mostrará los resultados del diagnóstico; los cuales mostrarán en dos secciones de manera general qué tan fortalecido se encuentra en cada componente y en particular qué áreas se tendrán que robustecerse en cada uno de ellos. La información generada le permitirá tomar decisiones sobre los aspectos a fortalecer en la empresa.</p>
	
